<section class="section-intro">
  <h2>GLR door de jaren heen</h2>
  <p>
    Het GLR beweegt al 75 jaar mee met de creatieve industrie. Deze mijlpalen laten zien hoe technologie, talent en ondernemerschap elkaar blijven versterken.
  </p>
</section>

